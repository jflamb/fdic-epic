#!/usr/bin/env node
// Bundle a single fdic-epic page script (e.g. support-intake.js) into a
// standalone, browser-loadable file.
//
// esbuild handles module parsing, dependency discovery, import/export rewriting,
// and npm package resolution. The static pages still load the generated output
// as a classic deferred script, so this emits an IIFE instead of an ESM file.
//
// Usage: node scripts/bundle-fdic-epic-page-script.mjs <entry-file>
// Example: node scripts/bundle-fdic-epic-page-script.mjs support-intake.js

import { build } from "esbuild";
import { basename, dirname, extname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SITE_DIR = resolve(__dirname, "../");

const entryArg = process.argv[2];
if (!entryArg) {
  console.error("Usage: node scripts/bundle-fdic-epic-page-script.mjs <entry-file>");
  process.exit(1);
}

const entryAbs = resolve(process.cwd(), entryArg);
const outputBase = basename(entryAbs, extname(entryAbs));
const outputAbs = resolve(SITE_DIR, `${outputBase}.bundle.js`);
const entryDisplayPath = relative(process.cwd(), entryAbs).replaceAll("\\", "/");
const outputDisplayPath = relative(process.cwd(), outputAbs).replaceAll("\\", "/");

const banner =
  `/* AUTO-GENERATED by scripts/bundle-fdic-epic-page-script.mjs - do not edit by hand.\n` +
  `   Regenerate with: node scripts/bundle-fdic-epic-page-script.mjs ${entryDisplayPath}\n` +
  `   Source: ${entryDisplayPath}\n` +
  `   Bundled with esbuild.\n*/`;

try {
  const result = await build({
    entryPoints: [entryAbs],
    outfile: outputAbs,
    bundle: true,
    format: "iife",
    platform: "browser",
    target: ["es2019"],
    banner: { js: banner },
    legalComments: "none",
    logLevel: "silent",
    metafile: true,
    sourcemap: false,
  });

  const inputCount = Object.keys(result.metafile.inputs).length;
  const output = result.metafile.outputs[outputDisplayPath];
  const bytes = output?.bytes ?? 0;
  console.log(
    `Wrote ${outputDisplayPath} - ${inputCount} input module${inputCount === 1 ? "" : "s"}, ${Math.round(bytes / 102.4) / 10} KB`,
  );
} catch (error) {
  console.error(error);
  process.exit(1);
}
