#!/usr/bin/env node
// Bundle every component module into a single file so each page makes one
// request instead of ~18. Uses a tiny custom packager that wraps each module
// in an IIFE and routes named imports/exports through a shared registry.
//
// Usage: node scripts/bundle-fdic-epic-components.mjs

import { readFile, writeFile, readdir } from "node:fs/promises";
import { join, dirname, resolve, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const COMPONENTS_DIR = resolve(__dirname, "../components");
const OUTPUT_FILE = join(COMPONENTS_DIR, "index.bundle.js");

const ENTRY = "index.js"; // imports drive discovery

// Side-effect imports must live on a single line so the lazy clause-import
// pattern can't accidentally swallow following imports while hunting for a
// downstream "from" keyword.
const sideEffectImportRegex = /^import\s+["']([^"']+)["'];?\s*$/gm;
const clauseImportRegex = /^import\s+([\s\S]+?)\s+from\s+["']([^"']+)["'];?\s*$/gm;
const exportNamedRegex = /^export\s+((?:async\s+)?(?:const|let|var|function\*?|class))\s+([A-Za-z_$][\w$]*)/gm;
const exportListRegex = /^export\s*\{\s*([^}]+)\s*\};?\s*$/gm;

async function readModule(absPath) {
  return await readFile(absPath, "utf8");
}

function resolveImport(fromAbs, spec) {
  if (!spec.startsWith(".")) {
    throw new Error(`Bare import not supported: ${spec} (in ${fromAbs})`);
  }
  return resolve(dirname(fromAbs), spec);
}

async function discoverModules(entryAbs) {
  const order = [];
  const visited = new Set();

  async function visit(absPath) {
    if (visited.has(absPath)) return;
    visited.add(absPath);
    const source = await readModule(absPath);
    const imports = new Set();
    for (const re of [sideEffectImportRegex, clauseImportRegex]) {
      re.lastIndex = 0;
      let m;
      while ((m = re.exec(source)) !== null) {
        const spec = re === sideEffectImportRegex ? m[1] : m[2];
        imports.add(resolveImport(absPath, spec));
      }
    }
    for (const dep of imports) {
      await visit(dep);
    }
    order.push({ absPath, source });
  }

  await visit(entryAbs);
  return order;
}

function moduleId(absPath) {
  return relative(COMPONENTS_DIR, absPath).replaceAll("\\", "/");
}

function transformModule(absPath, source) {
  const id = moduleId(absPath);
  const importLines = [];
  const exportRegistrations = [];

  // Side-effect imports: module registration runs at bundle init order.
  let body = source.replace(sideEffectImportRegex, (match, spec) => `/* import "${spec}" */`);

  // Clause imports: route through the registry.
  body = body.replace(clauseImportRegex, (match, clause, spec) => {
    const targetId = moduleId(resolveImport(absPath, spec));
    const trimmed = clause.trim();
    if (trimmed.startsWith("{")) {
      const names = trimmed.slice(1, -1).split(",").map((n) => n.trim()).filter(Boolean);
      const lines = names.map((entry) => {
        const [orig, aliased] = entry.split(/\s+as\s+/).map((s) => s.trim());
        const local = aliased || orig;
        return `const ${local} = __fdic_modules["${targetId}"].${orig};`;
      });
      importLines.push(...lines);
    } else if (trimmed.startsWith("*")) {
      const [, , alias] = trimmed.split(/\s+/);
      importLines.push(`const ${alias} = __fdic_modules["${targetId}"];`);
    } else {
      // Default import — rewrite to .default
      importLines.push(`const ${trimmed} = __fdic_modules["${targetId}"].default;`);
    }
    return `/* import handled */`;
  });

  // Capture `export const|let|var|function|class FOO ...` and rewrite to local declaration + register.
  body = body.replace(exportNamedRegex, (match, kind, name) => {
    exportRegistrations.push(`__fdic_modules["${id}"].${name} = ${name};`);
    return `${kind} ${name}`;
  });

  // Capture `export { a, b as c }`.
  body = body.replace(exportListRegex, (match, list) => {
    const entries = list.split(",").map((e) => e.trim()).filter(Boolean);
    for (const entry of entries) {
      const [orig, aliased] = entry.split(/\s+as\s+/).map((s) => s.trim());
      const exportedAs = aliased || orig;
      exportRegistrations.push(`__fdic_modules["${id}"].${exportedAs} = ${orig};`);
    }
    return `/* export list handled */`;
  });

  const importsBlock = importLines.length ? `  ${importLines.join("\n  ")}\n` : "";
  const exportsBlock = exportRegistrations.length ? `\n  ${exportRegistrations.join("\n  ")}` : "";
  return `// === ${id} ===\n(function(){\n  __fdic_modules["${id}"] = __fdic_modules["${id}"] || {};\n${importsBlock}${body}${exportsBlock}\n})();\n`;
}

async function main() {
  const entryAbs = join(COMPONENTS_DIR, ENTRY);
  const modules = await discoverModules(entryAbs);

  const header = `/* AUTO-GENERATED by scripts/bundle-fdic-epic-components.mjs — do not edit by hand.\n   Regenerate with: node scripts/bundle-fdic-epic-components.mjs\n   Source: ${modules.length} modules concatenated from components/.\n*/\n(function(){\n  if (window.__fdic_modules) return; // bundle already loaded\n  window.__fdic_modules = {};\n  const __fdic_modules = window.__fdic_modules;\n`;

  const body = modules.map(({ absPath, source }) => transformModule(absPath, source)).join("\n");

  const footer = `})();\n`;

  await writeFile(OUTPUT_FILE, header + body + footer, "utf8");
  console.log(`Wrote ${relative(process.cwd(), OUTPUT_FILE)} (${modules.length} modules)`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
